package com.example.jenkinsplugin

import com.intellij.lang.ASTNode
import com.intellij.lang.ParserDefinition
import com.intellij.lang.PsiParser
import com.intellij.lexer.Lexer
import com.intellij.openapi.project.Project
import com.intellij.psi.FileViewProvider
import com.intellij.psi.PsiElement
import com.intellij.psi.PsiFile
import com.intellij.psi.tree.IFileElementType
import com.intellij.psi.tree.TokenSet

class JenkinsParserDefinition : ParserDefinition {

    override fun createLexer(project: Project?): Lexer {
        return JenkinsLexer()
    }

    override fun createParser(project: Project?): PsiParser {
        return JenkinsParser()
    }

    override fun getFileNodeType(): IFileElementType {
        return FILE
    }

    override fun getCommentTokens(): TokenSet {
        return COMMENTS
    }

    override fun getStringLiteralElements(): TokenSet {
        return STRINGS
    }

    override fun createElement(node: ASTNode): PsiElement {
        return JenkinsPsiElement(node)
    }

    override fun createFile(viewProvider: FileViewProvider): PsiFile {
        return JenkinsPsiFile(viewProvider)
    }

    companion object {
        val FILE = IFileElementType(JenkinsLanguage.INSTANCE)
        val COMMENTS = TokenSet.create(JenkinsTokenTypes.COMMENT)
        val STRINGS = TokenSet.create(JenkinsTokenTypes.STRING)
    }
}
